    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        // TODO add your handling code here:
        String url = "jdbc:mysql://localhost:3306/library?useSSL=false";
        String mysqlUser = "root";
        String mysqlPass = "lilly2005";

        String col = (String) column.getSelectedItem();  
        String newValue = change.getText();              
        String id = idtext.getText();                    

        String query = "UPDATE admin SET " + col + "=? WHERE admin_id=?";

        try (Connection conn = DriverManager.getConnection(url, mysqlUser, mysqlPass);
             PreparedStatement ps = conn.prepareStatement(query)) {

            ps.setString(1, newValue);
            ps.setString(2, id);

            int rows = ps.executeUpdate();

            if (rows > 0) {
                JOptionPane.showMessageDialog(this, "Updated successfully!");
                displayActionPerformed(null);
            } else {
                JOptionPane.showMessageDialog(this, "No record updated!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_editActionPerformed

