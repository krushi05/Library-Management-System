    private void displayActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_displayActionPerformed
        // TODO add your handling code here:
        String url = "jdbc:mysql://localhost:3306/library?useSSL=false";
        String mysqlUser = "root";
        String mysqlPass = "lilly2005";

        String query = "SELECT * FROM admin"; 

        try (Connection conn = DriverManager.getConnection(url, mysqlUser, mysqlPass);
             Statement stmt = conn.createStatement();
             ResultSet rs = stmt.executeQuery(query)) {

            if (rs.next()) {
                fetchif.setVisible(true);

                idtext.setText(rs.getString("admin_id"));
                nametext.setText(rs.getString("aname"));
                pnotext.setText(rs.getString("pno"));
                passtext.setText(rs.getString("pass"));
            } else {
                JOptionPane.showMessageDialog(this, "No admin record found!");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }//GEN-LAST:event_displayActionPerformed

